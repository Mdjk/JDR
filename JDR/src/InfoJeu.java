
public class InfoJeu {
	private int lvlPlayers;
	private int diceType;
	private int encounterLvl;
	
	public int getLvlPlayers() {
		return lvlPlayers;
	}
	public void setLvlPlayers(int lvlPlayers) {
		this.lvlPlayers = lvlPlayers;
	}
	public int getDiceType() {
		return diceType;
	}
	public void setDiceType(int diceType) {
		this.diceType = diceType;
	}
	public int getEncounterLvl() {
		return encounterLvl;
	}
	public void setEncounterLvl(int encounterLvl) {
		this.encounterLvl = encounterLvl;
	}
	
	
}
